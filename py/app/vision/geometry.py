"""젬 가공 창 지오메트리 — 앵커("다음 항목 중 무작위로 적용됩니다.") 기준 상대 오프셋.

**모든 좌표는 1920×1080 / UI 스케일 100% 기준의 "기준 좌표"다.**
다른 해상도에서는 판독기가 앵커를 여러 배율로 찾아 배율을 확정한 뒤,
`scaled(REGIONS, scale)`로 좌표를 환산해 쓴다. 게임 UI는 세로 해상도에 비례해 커지고
가로는 여백만 늘어나므로(21:9·32:9), 단일 배율 하나로 전부 설명된다.

실측 샘플에서 앵커가 창 안 어디에 있든 상대 좌표가 유지됨을 확인했다.
"""


def scaled(regions: dict, scale: float) -> dict:
    """기준 좌표(1080p) → 현재 배율 좌표. scale=1.0이면 그대로."""
    if scale == 1.0:
        return dict(regions)
    return {name: tuple(round(v * scale) for v in box)
            for name, box in regions.items()}

